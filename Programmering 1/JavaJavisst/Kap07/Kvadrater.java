import javax.swing.*;
import java.awt.*;
//---------------------------------------------------------------------------------------------
// Skriv in vad programmet gör
//---------------------------------------------------------------------------------------------
public class Kvadrater extends JPanel {
//---------------------------------------------------------------------------------------------
// Deklarationer
//---------------------------------------------------------------------------------------------

//---------------------------------------------------------------------------------------------
// Konstruktor
//---------------------------------------------------------------------------------------------
	public Kvadrater(){
// Här dras bearbetningen igång
		
	}//end konstraktor
//---------------------------------------------------------------------------------------------
// Rita
//---------------------------------------------------------------------------------------------
	public void paint(Graphics g){
// System.out.println("paint()");
		
		g.clearRect(0,0,600,600);
		
		for(int i=0;i<500;i=i+10){
			g.drawRect(0+i,0+i,550-2*i,550-2*i);
		}//end for i

	}//end paint()
//---------------------------------------------------------------------------------------------
// Drar igång programmet
//---------------------------------------------------------------------------------------------
	public static void main(String[] args){
		JFrame f = new JFrame();
		f.setSize (600,600);
		f.setLocation(900,100);
		f.setTitle("Kvadrater");
		f.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		Kvadrater p = new Kvadrater();
		f.add(p);
		f.setVisible(true);
	}//end bearbeta()
}//end class


