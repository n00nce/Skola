import java.awt.*;
//---------------------------------------------------------------------------------------------
// Skriv in vad programmet gör
//---------------------------------------------------------------------------------------------
public abstract class Studs{
//---------------------------------------------------------------------------------------------
// Deklarationer
//---------------------------------------------------------------------------------------------
	double vx, vy, x, y;
	int WIDTH, HEIGHT, size;
//---------------------------------------------------------------------------------------------
// Konstruktor
//---------------------------------------------------------------------------------------------
	public Studs(){
// Här dras bearbetningen igång
		
		WIDTH 	= 400;
		HEIGHT 	= 400;
		x 	= WIDTH/2;
		y	= HEIGHT/2;
		size = WIDTH/20;
		vx 	= 2*Math.random()-1;
		vy 	= 2*Math.random()-1;
	}//end konstruktor
//---------------------------------------------------------------------------------------------
// Metoder som bearbetar -motor
//---------------------------------------------------------------------------------------------
	public void move(){
		x = x + vx;
		y = y + vy;
		
		if (x+size > WIDTH 	|| x < 0) vx = -vx;
		if (y+size > HEIGHT || y < 0) vy = -vy;
	}// end move()
//--------------------------------------------------------------------------------------------------
// Rita	
//--------------------------------------------------------------------------------------------------
	public abstract void draw (Graphics g);
}//end class