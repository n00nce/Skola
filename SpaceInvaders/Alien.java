//---------------------------------------------------------------------------------------------
public class Alien extends JPanel {
//---------------------------------------------------------------------------------------------
// Deklarationer
//---------------------------------------------------------------------------------------------
	int x= 190, y = 100;
	int vx = 2, vy = 1;
	Color color = Color.white;
	boolean destroyed = false;
	boolean first = true;
	boolean left = true;
//---------------------------------------------------------------------------------------------
// Konstruktor
//---------------------------------------------------------------------------------------------
	public Alien(){
	}//end konstruktor
//---------------------------------------------------------------------------------------------
// Metoder som bearbetar -motor
//---------------------------------------------------------------------------------------------
	public void move(){		
//System.out.println("move();");
		if (left) {
			x = x - vx;
		}//end if
		else {
			x = x + vx;
		}//end else	
	}// end move()
//---------------------------------------------------------------------------------------------
// Rita	
//---------------------------------------------------------------------------------------------
	public void draw(Graphics g, int i, int j) {
		if (first) {
			x = i;
			y = j;
			first = false;
		}
		else if (destroyed) {
			x = 1000;
			y = 1000;
		}
		g.setColor(color);
		g.fillRect(x,y, 20, 20);
	}//end draw(g, i, j)
}//end class