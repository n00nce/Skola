import javax.swing.*;
import java.awt.*;
//---------------------------------------------------------------------------------------------
// Skriv in vad programmet gör
//---------------------------------------------------------------------------------------------
public class Shot extends JPanel {
//---------------------------------------------------------------------------------------------
// Deklarationer
//---------------------------------------------------------------------------------------------
	int posX = 0, posY = 0, vy = 1;
	boolean moving = false;
	int x, y;
//---------------------------------------------------------------------------------------------
// Konstruktor
//---------------------------------------------------------------------------------------------
	public Shot(){
// Här dras bearbetningen igång

	}//end konstruktor
//---------------------------------------------------------------------------------------------
// Metoder som bearbetar -motor
//---------------------------------------------------------------------------------------------
	public void move(){
		vy = 1;
		posY = posY - vy;
		
		
	}// end move()
//---------------------------------------------------------------------------------------------
// Rita	
//---------------------------------------------------------------------------------------------
	public void draw(Graphics g, int i, int j) {
		x = i;
		y = j;
		if (moving==false) {
		posX = x;
		moving = true;
		}
		if (posY < 0) {
		posY = y;
		moving = false;
		}
		g.setColor(Color.white);
		g.fillRect(posX+5,posY, 10, 10);
	}//draw

}//end class