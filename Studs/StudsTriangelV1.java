import java.awt.*;
//---------------------------------------------------------------------------------------------
// Skriv in vad programmet gör
//---------------------------------------------------------------------------------------------
public class StudsTriangelV1 extends StudsOvalV1 {
//---------------------------------------------------------------------------------------------
// Deklarationer
//---------------------------------------------------------------------------------------------
	Point p[] = new Point[3];
//---------------------------------------------------------------------------------------------
// Konstruktor
//---------------------------------------------------------------------------------------------
	public StudsTriangelV1(){
// Här dras bearbetningen igång
		super();
		p[0] = new Point(0, size/2);
		p[1] = new Point(size, size/2);
		p[2] = new Point(size/2, 0);
	}//end konstruktor
//--------------------------------------------------------------------------------------------------
// Rita	
//--------------------------------------------------------------------------------------------------
	public void draw (Graphics g){
//System.out.println("Paint()");
		g.setColor(Color.white);
		g.drawLine((int)posX + p[0].x, (int)posY + p[0].y, (int)posX + p[1].x, (int)posY + p[1].y);
	}//end draw()
}//end class