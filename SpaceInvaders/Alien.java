public class Alien extends JPanel {
//---------------------------------------------------------------------------------------------
// Deklarationer
//---------------------------------------------------------------------------------------------
	int x= 190, y = 100;
	int vx = 2, vy = 1;
	Color color = Color.white;
	boolean destroyed = false;
	boolean first = true;
	boolean left = true;
//---------------------------------------------------------------------------------------------
// Konstruktor
//---------------------------------------------------------------------------------------------
	public Alien(){
// Här dras bearbetningen igång
		
	}//end konstruktor
//---------------------------------------------------------------------------------------------
// Metoder som bearbetar -motor
//---------------------------------------------------------------------------------------------
	public void move(){		
//		System.out.println("move();");
		if (left) {
		x = x - vx;
		}
		else{
			x = x + vx;
		}
		//y = y + vy;
		
	}// end move()
//---------------------------------------------------------------------------------------------
// Rita	
//---------------------------------------------------------------------------------------------
	public void draw(Graphics g, int i, int j) {
		/*
		x = x + i;
		y = y + j;
		*/
		if (destroyed) {
			x = 1000;
			y = 1000;
		}
		else if (first) {
		x = i;
		y = j;
		first = false;
		}
		else if(x == i-380){
			y=y+20;
			left = false;
		}
		else if(x>=700) {
			left=true;
		}
		g.setColor(color);
		g.fillRect(x,y, 20, 20);
		
	}//draw

}//end class